#include<iostream>

using namespace std;

int main()
{
    cout << "0 - 4 = " << 0 - 4 << "\n";
    cout << "3.14 x 2 = " << 3.14 * 2 << "\n";
    cout << "5 ÷ 3 = " << (double)5 / 3 << "\n";
    cout << "30 ÷ 7のあまり = " << 30 % 7 << "\n";
    cout << "(7 + 32) ÷ 5 = " << (7 + 32) / (double)5 << "\n";
}
